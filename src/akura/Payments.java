    private void txtPAYReg_IDKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtPAYReg_IDKeyReleased
        cmbPAYClass.removeAllItems();
        lblName.setText(null);
        try {
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select *, (select Name_with_initials from student where Reg_ID like '"+txtPAYReg_ID.getText().toUpperCase()+"%') as name from enroll where Std_Reg_No like '"+txtPAYReg_ID.getText()+"%'");
            while(rs.next()){
                cmbPAYClass.addItem(rs.getString("Class"));
                lblName.setText(rs.getString("name"));
                jLabel17.setText(rs.getString("Std_Reg_No"));
            }
        } catch (Exception e) {
            System.out.println(e);
        }
        
        
    }//GEN-LAST:event_txtPAYReg_IDKeyReleased

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        LocalDateTime lcldate = LocalDateTime.now();
        DateTimeFormatter df = DateTimeFormatter.ofPattern("yyyy-MMMM");
        
        try {
            PreparedStatement st = con.prepareStatement("insert into payment (Reg_ID, Class, Payment, Month) values('"+jLabel17.getText().toUpperCase()+"', '"+cmbPAYClass.getSelectedItem()+"', '"+txtPayment.getText()+"', '"+df.format(lcldate)+"')");
            st.executeUpdate();
            JOptionPane.showMessageDialog(rootPane, "Successfully Payment Accepted...", "Payment", JOptionPane.INFORMATION_MESSAGE);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "invalid you entered... Check again", "Payment", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

