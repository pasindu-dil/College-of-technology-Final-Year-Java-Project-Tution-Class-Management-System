    public ArrayList<PaymentData> paymentData(){
        ArrayList<PaymentData> datalist = new ArrayList<>();
        try {
            Connection con = DatabaseConnection.getcon();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("Select payment.*, student.Name_with_initials from payment inner join student on payment.Reg_ID = student.Reg_ID");
            PaymentData data;
            while(rs.next()){
                data = new PaymentData(rs.getString("Reg_ID"), rs.getString("Class"), rs.getString("Month"), rs.getInt("ID"),rs.getInt("Payment"), rs.getDate("Date"), rs.getString("Name_with_initials"));
                datalist.add(data);
            }
        } catch (Exception e) {
        }
        return datalist;
    }
    DefaultTableModel model = new DefaultTableModel();
    private void data(){
        ArrayList<PaymentData> list = paymentData();
        model = (DefaultTableModel) jTable1.getModel();
        Object row[] = new Object[7];
        for (int i = 0; i < list.size(); i++) {
            row[0] = list.get(i).getID();
            row[1] = list.get(i).getReg_ID();
            row[2] = list.get(i).getName_with_initials();
            row[3] = list.get(i).getClassP();
            row[4] = list.get(i).getPayment();
            row[5] = list.get(i).getMonth();
            row[6] = list.get(i).getDate();
            model.addRow(row);
        }
    }
    
    public void search(String str){
        model = (DefaultTableModel) jTable1.getModel();
        TableRowSorter<DefaultTableModel> trs = new TableRowSorter<>(model);
        jTable1.setRowSorter(trs);
        trs.setRowFilter(RowFilter.regexFilter(str));
    }
    
    
    
